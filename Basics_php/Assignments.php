<?php

// Assignment Operators

$x = 10;  // Assign 10 to $x
$y = 5;   // Assign 5 to $y

$x += $y; // Add $y to $x
echo $x;  // Outputs 15

$x -= $y; // Subtract $y from $x
echo $x;  // Outputs 10

$x *= $y; // Multiply $x by $y
echo $x;  // Outputs 50

$x /= $y; // Divide $x by $y
echo $x;  // Outputs 10

$x %= $y; // Get the remainder of $x divided by $y
echo $x;  // Outputs 0

?>